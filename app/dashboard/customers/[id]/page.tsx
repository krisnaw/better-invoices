export default async function CustomerDetailPage({params}: { params: Promise<{ id: number }> }) {
  const {id} = await params;

  const customer = await getCustomerById(id)

  if (!customer) {
    redirect('/dashboard/customers')
  }

  return (
    <div>
      <PageHeader
        title={customer.name}
        descriptions="A list of all the Customers in your account including their company name, contact, and email.">
        <Button>
          Add Customer
        </Button>
      </PageHeader>
      <InvoiceTable />
    </div>
  )
}


